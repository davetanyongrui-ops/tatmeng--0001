// Global translation switcher helper
document.addEventListener("DOMContentLoaded", () => {
    // 1. Get stored language or default to English
    let currentLang = localStorage.getItem("selectedLanguage") || "en";
    
    // Ensure fallback if stored value is invalid
    if (!translations[currentLang]) {
        currentLang = "en";
    }

    // 2. Function to translate elements with data-i18n attribute
    function translatePage(lang) {
        document.querySelectorAll("[data-i18n]").forEach(element => {
            const key = element.getAttribute("data-i18n");
            if (translations[lang] && translations[lang][key]) {
                const text = translations[lang][key];
                
                // If it contains HTML tag markers like <br/>, use innerHTML, otherwise use textContent
                if (text.includes("<") && text.includes(">")) {
                    element.innerHTML = text;
                } else {
                    element.textContent = text;
                }
            }
        });

        // Translate placeholders
        document.querySelectorAll("[data-i18n-placeholder]").forEach(element => {
            const key = element.getAttribute("data-i18n-placeholder");
            if (translations[lang] && translations[lang][key]) {
                element.setAttribute("placeholder", translations[lang][key]);
            }
        });

        // Translate alt texts
        document.querySelectorAll("[data-i18n-alt]").forEach(element => {
            const key = element.getAttribute("data-i18n-alt");
            if (translations[lang] && translations[lang][key]) {
                element.setAttribute("alt", translations[lang][key]);
            }
        });

        // Toggle page title if specific translation exists
        const pageName = window.location.pathname.split("/").pop().replace(".html", "") || "index";
        const pageTitleKey = "title-" + pageName;
        if (translations[lang] && translations[lang][pageTitleKey]) {
            document.title = translations[lang][pageTitleKey];
        }

        // Set active class on language select options/buttons
        document.querySelectorAll(".lang-selector-select").forEach(select => {
            select.value = lang;
        });

        // Save selection
        localStorage.setItem("selectedLanguage", lang);
        document.documentElement.setAttribute("lang", lang);
    }

    // 3. Bind language changes
    document.querySelectorAll(".lang-selector-select").forEach(select => {
        select.addEventListener("change", (e) => {
            translatePage(e.target.value);
        });
    });

    // 4. Initial translation apply
    translatePage(currentLang);
});
